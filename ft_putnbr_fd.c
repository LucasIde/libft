#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long n2;
	char n3;

	n2 = n;
	if (n2 < 0)
	{
		n2 *= -1;
		write(fd, "-", 1);
	}
	if (n2 > 9)
	{
		ft_putnbr_fd((n2 / 10), fd);
		n3 = (n2 % 10) + '0';
		write(fd, &n3, 1);
	}
	else
	{
		n3 = (n2 % 10) + '0';
		write(fd, &n3, 1);
	}
}
